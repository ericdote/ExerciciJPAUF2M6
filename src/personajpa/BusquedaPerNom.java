    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Persona_Controller pc = new Persona_Controller();
            Persona p = pc.BuscarPerNom(jTextField9.getText());
            jTextField1.setText(p.getNombre());
            jTextField2.setText(p.getApellidos());
            jTextField3.setText(p.getEmail());
            jTextField4.setText(p.getTelefono());
            jTextField5.setText(p.getDireccio().getCarrer());
            jTextField6.setText(p.getDireccio().getCiutat());
            jTextField7.setText(p.getDireccio().getCp());
            jTextField8.setText(p.getDireccio().getPais());
            jTextField9.setText("");
            BuidarTabla();
        } catch (NoResultException e) {
            BuidarCamps();
            JOptionPane.showMessageDialog(rootPane, "\"" + jTextField9.getText() + "\" no està al sistema");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

